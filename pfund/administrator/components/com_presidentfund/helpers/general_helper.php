                        RELATIONSHIP_TYPE_HUSBAND => 'Husband',
                        RELATIONSHIP_TYPE_WIFE => 'Wife',
                        RELATIONSHIP_TYPE_PERSON_SELF => 'Person Self'
        );
        
        if($option)
            $relations[0] = $option;
        
        ksort($relations);
        
        $relations[RELATIONSHIP_TYPE_ETC] = 'Etc.';
        
        return $relations;
    }
    
