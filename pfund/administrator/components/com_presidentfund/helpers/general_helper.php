class GeneralHelper
{
    function getTitles($option = '')
    {
        $titles = array(
                    TITLE_MR => 'Mr.',
                    TITLE_MISS => 'Miss.',
                    TITLE_MRS => 'Mrs.',
                    TITLE_MS => 'Ms.',
                    TITLE_MOST_VENERABLE => 'Most Venerable.',
                    TITLE_VENERABLE => 'Venerable.',
                    TITLE_REVERED_PRIEST => 'Revered Priest.',
                    TITLE_REVERED_SWAMY => 'Revered Swamy.',
                    TITLE_MOST_REV => 'Most Rev.',
                    TITLE_REV => 'Rev.',
                    TITLE_ASH_SHAIKH => 'Ash-Shaikh.',
                    TITLE_MAWLAVI => 'Mawlavi.'
        );
        
        if($option)
            $titles[-1] = $option;
        
        ksort($titles);
        
        $titles[TITLE_OTHER] = 'Other';
        
        return $titles;
    }
    
    function getRelationships($option = '')
    {
        $relations = array(
